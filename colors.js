  // 标准化ID格式（将 C1 转换为 C01）
  function normalizeColorId(colorId) {
    // 匹配格式：字母 + 数字（如 A1, B2, C10）
    const match = colorId.match(/^([A-Z]+)(\d+)$/);
    if (match) {
      const letter = match[1];
      const number = parseInt(match[2]);
      // 如果数字小于10，添加前导零
      if (number < 10) {
        return `${letter}0${number}`;
      }
    }
    return colorId;
  }
  
  // 根据MARD色号获取其他系统的色号
  function getColorCodeBySystem(mardId, system) {
    // 先尝试原始ID（兼容已有格式）
    if (colorCodeMapping[mardId]) {
      return colorCodeMapping[mardId][system] || null;
    }
    // 尝试标准化后的ID（将 C1 转换为 C01）
    const normalizedId = normalizeColorId(mardId);
    if (normalizedId !== mardId && colorCodeMapping[normalizedId]) {
      return colorCodeMapping[normalizedId][system] || null;
    }
    return null;
  }
  
  // 根据色号系统获取显示ID
  function getDisplayId(color, system) {
    if (system === 'MARD') {
      return color.id;
    }
    // 使用标准化后的ID查找映射
    const normalizedId = normalizeColorId(color.id);
    const code = getColorCodeBySystem(normalizedId, system);
    // 如果返回 "-"，表示没有对应色号
    if (code === '-') {
      return null;
    }
    return code || color.id;
  }
  
  // 根据色号系统获取分组（用于按新系统分组）
  function getGroupBySystem(color, system) {
    if (system === 'MARD') {
      return color.group;
    }
    // 使用标准化后的ID查找映射
    const normalizedId = normalizeColorId(color.id);
    const code = getColorCodeBySystem(normalizedId, system);
    // 如果返回 "-"，表示没有对应色号，返回 null
    if (!code || code === '-') {
      return null;
    }
    // 如果包含 "/"，取第一个作为分组依据
    let codeToUse = code;
    if (code.includes('/')) {
      codeToUse = code.split('/')[0].trim();
    }
    // 提取分组字母（如 E02 -> E, IC04 -> IC）
    if (/^[A-Z]+/.test(codeToUse)) {
      const match = codeToUse.match(/^([A-Z]+)/);
      if (match) {
        return match[1];
      }
    }
    // 如果是纯数字开头，按范围分组
    if (/^\d/.test(codeToUse)) {
      const numMatch = codeToUse.match(/^(\d+)/);
      if (numMatch) {
        const num = parseInt(numMatch[1]);
        // 按每50个数字一个范围分组：1-50, 51-100, 101-150, ...
        const rangeStart = Math.floor((num - 1) / 50) * 50 + 1;
        const rangeEnd = rangeStart + 49;
        return `数字${rangeStart}-${rangeEnd}`;
      }
      return '数字';
    }
    // 默认返回原分组
    return color.group;
  }
  
  // 查找最接近的颜色（返回最接近的1个和另外10个接近的颜色）
  // 只考虑用户已选择的颜色
  function findClosestColors(targetRgb, selectedColorIds = null) {
    const allColors = getAllColors();
    const selectedIds = selectedColorIds || getSelectedColorIds();
    
    // 如果用户没有选择任何颜色，返回所有颜色
    let filteredColors;
    if (selectedIds.size > 0) {
      // 用户选择了颜色，只返回已选择的颜色
      filteredColors = allColors.filter(color => {
        // 检查原始ID和标准化ID是否在已选列表中
        const normalizedId = normalizeColorId(color.id);
        return selectedIds.has(color.id) || selectedIds.has(normalizedId);
      });
    } else {
      // 用户没有选择任何颜色，返回所有颜色
      filteredColors = allColors;
    }
    
    // 如果过滤后没有颜色，返回所有颜色（避免没有结果）
    if (filteredColors.length === 0) {
      filteredColors = allColors;
    }
    
    const colorsWithDistance = filteredColors.map(color => ({
      ...color,
      distance: colorDistance(targetRgb, color.rgb),
      similarity: colorSimilarity(targetRgb, color.rgb)
    }));
    
    // 按距离排序
    colorsWithDistance.sort((a, b) => a.distance - b.distance);
    
    // 返回最接近的1个和另外10个（总共11个）
    return colorsWithDistance.slice(0, 11);
  }
  
